package enumeracao_composicao.entities;

public class Department {

	// Fields
	private String name;


	// Constructors
	public Department() {}	
	
	public Department(String name) {
		this.name = name;
	}

	// Getters and Setters
	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	
	
}
